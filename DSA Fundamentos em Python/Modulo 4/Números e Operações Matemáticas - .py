# Operações com números Float
print ('operações entre 2 numeros float, permanecem como float (3.1 + 6.4) = ', 3.1 + 6.4)
print ('operações com 1 numero float e 1 numeto int, permenecem como float(4 + 4.0) = ', 4 + 4.0)
print ('operações com 2 numeros int, permanecem como int (4 + 4) = ', 4 + 4)
print ('operações de divisão com 1 barra teremos um resultado float (4 / 2) = ',4 / 2)
print ('operações de divisão com 2 barras teremos um resultado int (4//3) = ',4 // 3)
print ('operações de divisão com 1 barra utilizando um numero int e um numero float nos retorna um numero float (4 / 3.0) = ',4 / 3.0)
print ('operações de divisão com 2 barras utilizando um numero float e um int nos retornara um numero float (4 // 3.0) = ',4 // 3.0)

